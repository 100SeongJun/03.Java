package step01_syntax;

public class Ex03Operation {
	public static void main(String[] args) {
		//숫자연산 : + - *, /
		int v1 =10;
		int v2 =3;
	System.out.println("v1: "+ ++v1);	
	System.out.println("v1: "+ v1 ++);
	System.out.println("v1: "+v1);
	System.out.println("v2: "+--v2);
	System.out.println("v2: "+v2--);
	System.out.println("v2: "+v2);
	}
}